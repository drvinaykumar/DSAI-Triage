// Counter animation for stats
function animateCounter(element, target, prefix = '', suffix = '', duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    const timer = setInterval(() => {
        start += increment;
        if (start >= target) {
            element.textContent = prefix + target + suffix;
            clearInterval(timer);
        } else {
            element.textContent = prefix + Math.floor(start) + suffix;
        }
    }, 16);
}

// Trigger counter animation when stats come into view
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {            const statNumber = entry.target.querySelector(".stat-number");
            const targetValue = parseFloat(statNumber.dataset.target);
            const prefix = statNumber.dataset.prefix || "";
            const suffix = statNumber.dataset.suffix || "";

            if (!isNaN(targetValue)) {
                animateCounter(statNumber, targetValue, prefix, suffix);
            }
            statsObserver.unobserve(entry.target);
        }
    });
});

document.querySelectorAll('.stat-item').forEach(stat => {
    statsObserver.observe(stat);
});

